package Operator;

public class Arithmetic {


	public static void main(String[] args)
	{
	System.out.println("Arithmetic Operators");
	
	int a,b,c;
	a=43;
	b=5;
	c=a+b;
	System.out.println(c);
	
	c=a-b;
	System.out.println(c);
	
	c=a*b;
	System.out.println(c);
	
	c=a%b;
	System.out.println(c);
	
	c=a/b;
	System.out.println(c);
	
	}
}

