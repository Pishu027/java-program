package Operator;

public class ternary {

	public static void main(String[] args) {
		
		System.out.println("Ternary Operator");
		
		int a=20, b=15;
		System.out.println("Value of A =  "+a);

		System.out.println("Value of B =  "+b);
		
	int r=	(a<b?b:a);
	System.out.println(r);
	}

}
