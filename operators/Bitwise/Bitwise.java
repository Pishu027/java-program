package Operator;

public class Bitwise {

	public static void main(String[] args) {
		System.out.println("Bitwise Operator");
		
		int a=12,b=15;
		
		System.out.println("Value of A="+a);
		
		System.out.println("Value of B="+b);
		
		System.out.println("a & b = "+(a&b));
		
		System.out.println("a | b = "+(a|b));
		

	}

}
