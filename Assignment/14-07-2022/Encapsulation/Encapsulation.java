package demo;

public class Encapsulation {
	private double percentage;
	private char grade;

	public void setPercentage(double percentage)
	{
		this.percentage = percentage;
	}
	
	public double getPercentage() 
	{
		return percentage;
	}

	public char getGrade() {
		return grade;
	}

	public void setGrade(char grade) {
		this.grade = grade;
	}
	
}
